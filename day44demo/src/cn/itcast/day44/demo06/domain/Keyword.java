package cn.itcast.day44.demo06.domain;

public class Keyword {
	
	private String id;
	private String chineseword;
	private String pinyin;
	private String jianxie;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getChineseword() {
		return chineseword;
	}
	public void setChineseword(String chineseword) {
		this.chineseword = chineseword;
	}
	public String getPinyin() {
		return pinyin;
	}
	public void setPinyin(String pinyin) {
		this.pinyin = pinyin;
	}
	public String getJianxie() {
		return jianxie;
	}
	public void setJianxie(String jianxie) {
		this.jianxie = jianxie;
	}
}
